def _find_outlier_row_ids(rows: list[dict[str, Any]], recent_median_value: Optional[float]) -> tuple[list[str], list[str]]:
    if not rows:
        return ([], [])

    priced_rows: list[tuple[float, str]] = []
    for idx, row in enumerate(rows):
        price = row.get("price_value")
        if price is None:
            continue
        try:
            price_value = float(price)
        except (TypeError, ValueError):
            continue
        row_id = str(row.get("_row_id") or f"row-{idx}")
        priced_rows.append((price_value, row_id))

    if not priced_rows:
        return ([], [])

    high_value = max(price for price, _ in priced_rows)
    low_value = min(price for price, _ in priced_rows)

    high_ids = [row_id for price, row_id in priced_rows if price == high_value]
    low_ids = [row_id for price, row_id in priced_rows if price == low_value]
    return (high_ids, low_ids)


