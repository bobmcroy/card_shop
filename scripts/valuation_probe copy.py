def load_csv_rows(path: Path) -> list[dict[str, Any]]:
    if not path.exists():
        raise FileNotFoundError(f"Normalized comps CSV not found: {path}")

    rows: list[dict[str, Any]] = []
    with path.open("r", newline="", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for raw in reader:
            row = dict(raw)

            for key in ["price_value", "shipping_value", "total_value", "grade_value"]:
                row[key] = _parse_optional_number(row.get(key))

            row["is_best_offer"] = _parse_boolish(row.get("is_best_offer"))
            row["is_graded"] = _parse_boolish(row.get("is_graded"))

            rows.append(row)

    return rows


def infer_search_context_from_filename(path: Path) -> dict[str, str]:
    stem = path.stem
    if stem.endswith("_normalized"):
        stem = stem[:-11]

    parts = stem.split("_")
    context = {
        "sport": parts[0] if len(parts) > 0 else "",
        "year": parts[1] if len(parts) > 1 else "",
        "set_guess": " ".join(parts[2:]) if len(parts) > 2 else "",
    }
    return context


def _get_top_grade_label(grader: str, grade_value: Optional[float]) -> Optional[str]:
    g = normalize_grader(grader)

    if g == "PSA" and grade_value == 10.0:
        return "PSA 10"
    if g == "SGC" and grade_value == 10.0:
        return "SGC 10"
    if g == "CGC" and grade_value == 10.0:
        return "CGC 10"
    if g == "CSG" and grade_value == 10.0:
        return "CSG 10"
    if g == "BGS" and grade_value == 10.0:
        return "BGS 10"
    if g == "BGS" and grade_value == 9.5:
        return "BGS 9.5"

    return None


def _is_top_grade_filter(grader: str, grade_value: Optional[float]) -> bool:
    return _get_top_grade_label(grader, grade_value) is not None


def _count_top_grade_filtered_rows(
    rows: list[dict[str, Any]],
    grader: str,
    grade_value: Optional[float],
) -> int:
    target_grader = normalize_grader(grader)
    count = 0
    for row in rows:
        if (
            normalize_grader(row.get("grader_norm")) == target_grader
            and row.get("grade_value") == grade_value
        ):
            count += 1
    return count


def build_scarcity_adjustment(
    *,
    grader: str,
    grade_value: Optional[float],
    top_grade_pop: Optional[int],
    selection_model_name: str,
    filtered_rows: list[dict[str, Any]],
    strong_bin_count: int,
    blended_value: Optional[float],
) -> ScarcityAdjustmentSummary:
    normalized_grader = normalize_grader(grader)
    top_grade_label = _get_top_grade_label(normalized_grader, grade_value) or "(not applicable)"

    if not _is_top_grade_filter(normalized_grader, grade_value):
        return ScarcityAdjustmentSummary(
            grader=normalized_grader,
            grade_value=grade_value,
            top_grade_pop=top_grade_pop,
            top_grade_label=top_grade_label,
            eligible=False,
            applied=False,
            market_supported=False,
            scarcity_tier="not applicable",
            premium_pct=0.0,
            adjusted_high_value=None,
            seller_max_value=None,
            reason="Scarcity influence is only evaluated for top-grade valuations from major graders "
            "(PSA 10, SGC 10, BGS 9.5/10, CGC 10, CSG 10).",
        )

    if top_grade_pop is None:
        return ScarcityAdjustmentSummary(
            grader=normalized_grader,
            grade_value=grade_value,
            top_grade_pop=None,
            top_grade_label=top_grade_label,
            eligible=True,
            applied=False,
            market_supported=False,
            scarcity_tier="none",
            premium_pct=0.0,
            adjusted_high_value=None,
            seller_max_value=None,
            reason=f"No {top_grade_label} population was provided, so no scarcity adjustment was applied.",
        )

    if selection_model_name != MODEL_B_NAME:
        return ScarcityAdjustmentSummary(
            grader=normalized_grader,
            grade_value=grade_value,
            top_grade_pop=top_grade_pop,
            top_grade_label=top_grade_label,
            eligible=True,
            applied=False,
            market_supported=False,
            scarcity_tier="none",
            premium_pct=0.0,
            adjusted_high_value=None,
            seller_max_value=None,
            reason="Scarcity adjustment requires a high-liquidity Model B selection.",
        )

    top_grade_comp_count = _count_top_grade_filtered_rows(filtered_rows, normalized_grader, grade_value)
    market_supported = top_grade_comp_count >= 2 or (top_grade_comp_count >= 1 and strong_bin_count > 0)
    if not market_supported:
        return ScarcityAdjustmentSummary(
            grader=normalized_grader,
            grade_value=grade_value,
            top_grade_pop=top_grade_pop,
            top_grade_label=top_grade_label,
            eligible=True,
            applied=False,
            market_supported=False,
            scarcity_tier="none",
            premium_pct=0.0,
            adjusted_high_value=None,
            seller_max_value=None,
            reason=(
                f"{top_grade_label} scarcity needs market support "
                "(at least 2 matching graded comps, or 1 comp plus strong BIN support)."
            ),
        )

    if top_grade_pop <= 10:
        scarcity_tier = "strong"
        premium_pct = 0.15
    elif top_grade_pop <= 25:
        scarcity_tier = "moderate"
        premium_pct = 0.10
    elif top_grade_pop <= 50:
        scarcity_tier = "light"
        premium_pct = 0.05
    else:
        return ScarcityAdjustmentSummary(
            grader=normalized_grader,
            grade_value=grade_value,
            top_grade_pop=top_grade_pop,
            top_grade_label=top_grade_label,
            eligible=True,
            applied=False,
            market_supported=True,
            scarcity_tier="none",
            premium_pct=0.0,
            adjusted_high_value=None,
            seller_max_value=None,
            reason=f"{top_grade_label} population {top_grade_pop} does not qualify for a scarcity premium.",
        )

    if blended_value is None:
        return ScarcityAdjustmentSummary(
            grader=normalized_grader,
            grade_value=grade_value,
            top_grade_pop=top_grade_pop,
            top_grade_label=top_grade_label,
            eligible=True,
            applied=False,
            market_supported=True,
            scarcity_tier=scarcity_tier,
            premium_pct=0.0,
            adjusted_high_value=None,
            seller_max_value=None,
            reason="Base blended valuation was unavailable, so no scarcity premium was applied.",
        )

    adjusted_high_value = round(blended_value * (1.0 + premium_pct), 2)
    seller_max_value = round(adjusted_high_value * 1.05, 2)

    return ScarcityAdjustmentSummary(
        grader=normalized_grader,
        grade_value=grade_value,
        top_grade_pop=top_grade_pop,
        top_grade_label=top_grade_label,
        eligible=True,
        applied=True,
        market_supported=True,
        scarcity_tier=scarcity_tier,
        premium_pct=premium_pct,
        adjusted_high_value=adjusted_high_value,
        seller_max_value=seller_max_value,
        reason=(
            f"{top_grade_label} population {top_grade_pop} with Model B liquidity and supporting comps "
            f"yields a {scarcity_tier} scarcity signal."
        ),
    )



def _round_display_value(value: Any) -> Optional[int]:
    if value is None:
        return None
    try:
        return int(float(value) + 0.5)
    except (TypeError, ValueError):
        return None


def _fmt_money(value: Any) -> str:
    rounded = _round_display_value(value)
    if rounded is None:
        return "(none)"
    return f"${rounded:,}"


def _format_range(low: Any, mid: Any, high: Any) -> str:
    low_s = _fmt_money(low)
    mid_s = _fmt_money(mid)
    high_s = _fmt_money(high)
    return f"{low_s}-{mid_s}-{high_s}"


def _parse_range_values(range_text: str | None) -> tuple[Optional[float], Optional[float], Optional[float]]:
    text = _norm(range_text)
    if not text:
        return (None, None, None)
    parts = [p.strip().replace("$", "").replace(",", "") for p in text.split("-")]
    if len(parts) != 3:
        return (None, None, None)
    values: list[Optional[float]] = []
    for part in parts:
        try:
            values.append(float(part))
        except ValueError:
            values.append(None)
    return (values[0], values[1], values[2])


def _grade_label(value: Optional[float]) -> str:
    if value is None:
        return ""
    if float(value).is_integer():
        return str(int(value))
    return str(value)


def _infer_lane_label(grader: str, grade_value: Optional[float], require_graded: Optional[bool]) -> str:
    normalized = normalize_grader(grader)
    if require_graded is False:
        return "eBay Raw"
    if normalized and grade_value is not None:
        return f"eBay {normalized} {_grade_label(grade_value)}"
    if normalized:
        return f"eBay {normalized}"
    if require_graded is True:
        return "eBay Graded"
    return "eBay Raw"


def _infer_grader_note(grader: str, grade_value: Optional[float], require_graded: Optional[bool]) -> Optional[str]:
    normalized = normalize_grader(grader)
    if normalized and grade_value is not None:
        return f"{normalized} {_grade_label(grade_value)}"
    if normalized:
        return normalized
    if require_graded is True:
        return "graded only"
    if require_graded is False:
        return "raw only"
    return None


