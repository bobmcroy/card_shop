def _norm(value: str | None) -> str:
    return " ".join((value or "").strip().split())


def _safe_lower(value: str | None) -> str:
    return _norm(value).lower()


def _shorten(text: str | None, max_len: int = 60) -> str:
    value = _norm(text)
    if len(value) <= max_len:
        return value
    return value[: max_len - 3] + "..."


def _parse_optional_float(value: str | None) -> Optional[float]:
    text = _norm(value)
    if not text:
        return None
    try:
        return float(text)
    except ValueError as exc:
        raise ValueError(f"Could not parse float from: {value!r}") from exc


def _parse_boolish(value: Any) -> Any:
    if isinstance(value, bool):
        return value
    if value is None:
        return None
    text = _safe_lower(str(value))
    if text in {"true", "t", "yes", "y", "1"}:
        return True
    if text in {"false", "f", "no", "n", "0"}:
        return False
    return value


def _parse_optional_number(value: Any) -> Any:
    if value is None:
        return None
    if isinstance(value, (int, float)):
        return value
    text = _norm(str(value))
    if not text:
        return None
    try:
        return float(text)
    except ValueError:
        return value


def normalize_grader(value: str | None) -> str:
    return _safe_lower(value).upper()


def coerce_inherited_grade_value(value: Any) -> Optional[float]:
    if value in (None, ""):
        return None
    if isinstance(value, (int, float)):
        return float(value)
    return _parse_optional_float(str(value))


