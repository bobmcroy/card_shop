def _outlier_summary_html(lane: Optional[LaneResult]) -> str:
    tooltip = (
        "High outlier is the highest observed sale in this lane. "
        "Low outlier is the lowest observed sale in this lane."
    )
    if lane is None:
        high_part = '<span class="outlier-label">High outlier:</span> <span class="outlier-value">—</span>'
        low_part = '<span class="outlier-label">Low outlier:</span> <span class="outlier-value">—</span>'
    else:
        high_target = lane.outlier_high_rows[0] if lane.outlier_high_rows else ""
        low_target = lane.outlier_low_rows[0] if lane.outlier_low_rows else ""

        high_value = None
        if high_target:
            for row in lane.rows:
                if str(row.get("_row_id") or "") == high_target:
                    high_value = row.get("price_value")
                    break
        low_value = None
        if low_target:
            for row in lane.rows:
                if str(row.get("_row_id") or "") == low_target:
                    low_value = row.get("price_value")
                    break

        high_money = html_lib.escape(_fmt_money(high_value))
        low_money = html_lib.escape(_fmt_money(low_value))

        if high_target:
            high_value_html = f'<a class="outlier-link up" href="#{html_lib.escape(high_target)}">{high_money}</a>'
        else:
            high_value_html = f'<span class="outlier-value">{high_money}</span>'

        if low_target:
            low_value_html = f'<a class="outlier-link down" href="#{html_lib.escape(low_target)}">{low_money}</a>'
        else:
            low_value_html = f'<span class="outlier-value">{low_money}</span>'

        high_part = f'<span class="outlier-label">High outlier:</span> {high_value_html}'
        low_part = f'<span class="outlier-label">Low outlier:</span> {low_value_html}'

    return (
        f'<div class="outlier-summary">'
        f'<span class="outlier-part">{high_part}</span>'
        f'<span class="outlier-separator">|</span>'
        f'<span class="outlier-part">{low_part}</span>'
        f'</div>'
    )


