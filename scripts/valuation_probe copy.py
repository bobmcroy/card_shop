def print_scarcity_report(summary: ScarcityAdjustmentSummary) -> None:
    print_section("Scarcity Signal")
    if not summary.eligible:
        print(f"Not applicable: {summary.reason}")
        return
    print(f"Eligible:                  {'yes' if summary.eligible else 'no'}")
    print(f"{summary.top_grade_label} population:         {summary.top_grade_pop if summary.top_grade_pop is not None else '(none)'}")
    print(f"Market supported:          {'yes' if summary.market_supported else 'no'}")
    print(f"Scarcity tier:             {summary.scarcity_tier}")
    print(f"Premium pct:               {summary.premium_pct:.0%}")
    print(f"Adjusted high-side value:  {summary.adjusted_high_value if summary.adjusted_high_value is not None else '(none)'}")
    print(f"Seller max value:          {summary.seller_max_value if summary.seller_max_value is not None else '(none)'}")
