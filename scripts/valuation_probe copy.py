def _group_rows_for_valuation(
    rows: list[dict[str, Any]],
    *,
    grader: str,
    grade_value: Optional[float],
    require_graded: Optional[bool],
) -> list[dict[str, Any]]:
    normalized_grader = normalize_grader(grader)
    # Explicit lane: keep as single lane.
    if normalized_grader or grade_value is not None or require_graded is False:
        return [
            {
                "lane_label": _infer_lane_label(normalized_grader, grade_value, require_graded),
                "rows": rows,
                "lane_grader": normalized_grader,
                "lane_grade_value": grade_value,
                "lane_require_graded": require_graded,
            }
        ]

    # Generic broad search with graded only: split by exact grader+grade.
    if require_graded is True:
        grouped: dict[tuple[str, Optional[float]], list[dict[str, Any]]] = {}
        for row in rows:
            g = normalize_grader(row.get("grader_norm"))
            gv = row.get("grade_value")
            key = (g or "GRADED", gv)
            grouped.setdefault(key, []).append(row)
        groups = []
        for (g, gv), lane_rows in sorted(grouped.items(), key=lambda item: (item[0][0], item[0][1] if item[0][1] is not None else -1), reverse=False):
            groups.append(
                {
                    "lane_label": _infer_lane_label(g, gv, True),
                    "rows": lane_rows,
                    "lane_grader": g,
                    "lane_grade_value": gv,
                    "lane_require_graded": True,
                }
            )
        return groups

    # Mixed generic search: raw + exact graded lanes.
    raw_rows = [r for r in rows if not normalize_grader(r.get("grader_norm"))]
    graded_rows = [r for r in rows if normalize_grader(r.get("grader_norm"))]
    groups: list[dict[str, Any]] = []
    if raw_rows:
        groups.append(
            {
                "lane_label": "eBay Raw",
                "rows": raw_rows,
                "lane_grader": "",
                "lane_grade_value": None,
                "lane_require_graded": False,
            }
        )
    graded_grouped: dict[tuple[str, Optional[float]], list[dict[str, Any]]] = {}
    for row in graded_rows:
        g = normalize_grader(row.get("grader_norm"))
        gv = row.get("grade_value")
        graded_grouped.setdefault((g, gv), []).append(row)
    for (g, gv), lane_rows in sorted(graded_grouped.items(), key=lambda item: (item[0][0], item[0][1] if item[0][1] is not None else -1), reverse=False):
        groups.append(
            {
                "lane_label": _infer_lane_label(g, gv, True),
                "rows": lane_rows,
                "lane_grader": g,
                "lane_grade_value": gv,
                "lane_require_graded": True,
            }
        )
    return groups or [
        {
            "lane_label": "eBay Raw",
            "rows": rows,
            "lane_grader": "",
            "lane_grade_value": None,
            "lane_require_graded": require_graded,
        }
    ]


def _best_card_image_data_uri(rows: list[dict[str, Any]]) -> Optional[str]:
    image_fields = [
        "image_url",
        "img_url",
        "picture_url",
        "gallery_url",
        "galleryURL",
        "pictureURL",
        "thumbnail_url",
        "image",
        "img",
    ]

    # Prefer direct image fields from the normalized/scraped rows.
    for row in rows[:20]:
        image_url = ""
        for field in image_fields:
            image_url = _norm(str(row.get(field) or ""))
            if image_url:
                break
        if not image_url:
            continue

        try:
            req = urllib.request.Request(
                image_url,
                headers={"User-Agent": "Mozilla/5.0", "Referer": "https://www.ebay.com/"},
            )
            with urllib.request.urlopen(req, timeout=5) as resp:
                data = resp.read()
                content_type = resp.headers.get_content_type() or "image/jpeg"
            if data:
                encoded = base64.b64encode(data).decode("ascii")
                return f"data:{content_type};base64,{encoded}"
        except Exception:
            # Fallback: if embedding fails, still use the direct image URL.
            return image_url

    return None


