def choose_model_from_selection(selection_name: str):
    if selection_name == MODEL_B_NAME:
        return MODEL_B
    return MODEL_A


def resolve_filter_inputs(
    inherited_filters: Optional[dict[str, Any]] = None,
) -> tuple[str, str, str, Optional[float], Optional[bool], str, Optional[int]]:
    inherited_filters = inherited_filters or {}

    inherited_player_name = _norm(str(inherited_filters.get("player_name", "")))
    inherited_card_number = _norm(str(inherited_filters.get("card_number", "")))
    inherited_grader = normalize_grader(inherited_filters.get("grader"))
    inherited_grade_value = coerce_inherited_grade_value(inherited_filters.get("grade_value"))
    inherited_require_graded = inherited_filters.get("require_graded", None)

    launched_from_scraper = any(
        [
            inherited_player_name,
            inherited_card_number,
            inherited_grader,
            inherited_grade_value is not None,
            inherited_require_graded is not None,
        ]
    )

    if launched_from_scraper:
        player_name = inherited_player_name
        card_number = inherited_card_number
        grader = inherited_grader
        grade_value = inherited_grade_value
        require_graded = inherited_require_graded
    else:
        player_name = prompt_player_name_optional()
        card_number = prompt_card_number_optional()
        grader = prompt_grade_company_optional()
        grade_value = prompt_grade_value_optional()
        require_graded = prompt_require_graded_optional()

    manual_override = prompt_manual_model_override_optional()

    top_grade_pop: Optional[int] = None
    top_grade_label = _get_top_grade_label(grader, grade_value)
    if top_grade_label:
        top_grade_pop = prompt_top_grade_population_optional(top_grade_label)

    return (
        player_name,
        card_number,
        grader,
        grade_value,
        require_graded,
        manual_override,
        top_grade_pop,
    )



def _run_lane_valuation(
    *,
    sport: str,
    year_filter: str,
    player_name: str,
    card_number: str,
    manual_override: str,
    top_grade_pop: Optional[int],
    lane_label: str,
    lane_rows: list[dict[str, Any]],
    lane_grader: str,
    lane_grade_value: Optional[float],
    lane_require_graded: Optional[bool],
) -> tuple[list[dict[str, Any]], LaneResult]:
    print_filtered_rows_report(lane_rows)
    buckets = build_comp_buckets(lane_rows)
    bucket_info = bucket_summary(buckets)
    # print rounded bucket values
    bucket_info["recent_median_value"] = _fmt_money(buckets.recent_median_value) if buckets.recent_median_value is not None else "(none)"
    bucket_info["strong_bin_value"] = _fmt_money(buckets.strong_bin_value) if buckets.strong_bin_value is not None else "(none)"
    bucket_info["peak_auction_value"] = _fmt_money(buckets.peak_auction_value) if buckets.peak_auction_value is not None else "(none)"
    print_bucket_report(bucket_info)

    selector_input = LiquiditySelectorInput(
        sport=sport,
        year=int(year_filter) if year_filter.isdigit() else 0,
        set_name="",
        player_name=player_name or None,
        card_number=card_number or None,
        filtered_comp_count=len(lane_rows),
        recent_median_value=buckets.recent_median_value,
        strong_bin_value=buckets.strong_bin_value,
        peak_auction_value=buckets.peak_auction_value,
        manual_model_override=manual_override or None,
    )
    selection = select_liquidity_model(selector_input)
    print_selector_report(selection)

    selected_model = choose_model_from_selection(selection.selected_model_name)
    valuation = apply_liquidity_model(buckets, selected_model, rebalance_missing=False)

    scarcity_summary = build_scarcity_adjustment(
        grader=lane_grader,
        grade_value=lane_grade_value,
        top_grade_pop=top_grade_pop,
        selection_model_name=selection.selected_model_name,
        filtered_rows=lane_rows,
        strong_bin_count=int(bucket_info.get("strong_bin_count") or 0),
        blended_value=valuation.blended_value,
    )

    display_result = valuation
    print_valuation_report(display_result)
    print_scarcity_report(scarcity_summary)

    low_value = buckets.recent_median_value
    mid_value = valuation.blended_value
    high_value = scarcity_summary.adjusted_high_value if scarcity_summary.adjusted_high_value is not None else valuation.blended_value
    valuation_range = _format_range(low_value, mid_value, high_value)
    valuation_note = _build_valuation_note(
        lane_label=lane_label,
        model_label=selection.selected_model_name,
        comp_count=len(lane_rows),
        grader_note=_infer_grader_note(lane_grader, lane_grade_value, lane_require_graded),
        scarcity_pct=scarcity_summary.premium_pct,
    )

    outlier_high_rows, outlier_low_rows = _find_outlier_row_ids(lane_rows, buckets.recent_median_value)

    lane_result = LaneResult(
        lane_label=lane_label,
        grader=lane_grader,
        grade_value=lane_grade_value,
        rows=lane_rows,
        valuation_range=valuation_range,
        valuation_note=valuation_note,
        recent_median_value=buckets.recent_median_value,
        blended_value=valuation.blended_value,
        seller_max_value=scarcity_summary.seller_max_value,
        selected_model_name=selection.selected_model_name,
        scarcity_summary=scarcity_summary,
        outlier_high_rows=outlier_high_rows,
        outlier_low_rows=outlier_low_rows,
    )

    print_filtered_rows_report(lane_rows, lane_label=lane_label, valuation_range=valuation_range, valuation_note=valuation_note)
    return lane_rows, lane_result


def run_valuation_flow(
    *,
    normalized_csv_path: Path,
    sport: str,
    year_filter: str = "",
    inherited_filters: Optional[dict[str, Any]] = None,
) -> None:
    (
        player_name,
        card_number,
        grader,
        grade_value,
        require_graded,
        manual_override,
        top_grade_pop,
    ) = resolve_filter_inputs(inherited_filters)

    print_selected_file_context(
        sport=sport,
        year_filter=year_filter,
        csv_path=normalized_csv_path,
    )

    print_filter_context(
        player_name=player_name,
        card_number=card_number,
        grader=grader,
        grade_value=grade_value,
        require_graded=require_graded,
    )

    normalized_rows = load_csv_rows(normalized_csv_path)
    for idx, row in enumerate(normalized_rows):
        row["_row_id"] = f"comp-{idx+1}"
    print(f"\nLoaded normalized rows: {len(normalized_rows)}")

    criteria = CompFilterCriteria(
        sport=sport,
        year=int(year_filter) if year_filter.isdigit() else 0,
        set_name="",
        player_name=player_name or None,
        card_number=card_number or None,
        grader=grader or None,
        grade_value=grade_value,
        require_graded=require_graded,
    )

    filtered_rows = filter_comps(normalized_rows, criteria)
    print(f"Filtered rows:          {len(filtered_rows)}")

    if not filtered_rows:
        print("\nNo filtered comps matched the current criteria.")
        print("Try broadening the inputs:")
        print(" - remove grader/grade filters")
        print(" - clear card #")
        print(" - shorten player name")
        print(" - choose a broader normalized CSV")
        return

    groups = _group_rows_for_valuation(
        filtered_rows,
        grader=grader,
        grade_value=grade_value,
        require_graded=require_graded,
    )

    lane_results: list[LaneResult] = []
    valuation_export_rows: list[dict[str, Any]] = []

    for index, group in enumerate(groups):
        if len(groups) > 1:
            print_section(f"Valuation Lane {index + 1}: {group['lane_label']}")
        _, lane_result = _run_lane_valuation(
            sport=sport,
            year_filter=year_filter,
            player_name=player_name,
            card_number=card_number,
            manual_override=manual_override,
            top_grade_pop=top_grade_pop,
            lane_label=group["lane_label"],
            lane_rows=group["rows"],
            lane_grader=group["lane_grader"],
            lane_grade_value=group["lane_grade_value"],
            lane_require_graded=group["lane_require_graded"],
        )
        lane_results.append(lane_result)
        valuation_export_rows.extend(
            _build_filtered_row_display(
                group["rows"],
                lane_result.lane_label,
                lane_result.valuation_range,
                lane_result.valuation_note,
            )
        )

    valuation_csv_path = normalized_csv_path.with_name(normalized_csv_path.name.replace("_normalized.csv", "_valuation.csv"))
    valuation_json_path = normalized_csv_path.with_name(normalized_csv_path.name.replace("_normalized.csv", "_valuation.json"))
    valuation_html_path = normalized_csv_path.with_name(normalized_csv_path.name.replace("_normalized.csv", "_valuation_report.html"))

    with valuation_csv_path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=FILTERED_ROW_COLUMNS)
        writer.writeheader()
        writer.writerows(valuation_export_rows)
    print(f"Valuation CSV written: {valuation_csv_path}")

    payload = {
        "card_title": (str((inherited_filters or {}).get("search_keywords") or (inherited_filters or {}).get("search_string") or " ".join(part for part in [year_filter, player_name, card_number] if part).strip() or normalized_csv_path.stem)).upper(),
        "source_csv": str(normalized_csv_path),
        "lanes": [
            {
                "lane_label": lane.lane_label,
                "grader": lane.grader,
                "grade_value": lane.grade_value,
                "valuation_range": lane.valuation_range,
                "valuation_note": lane.valuation_note,
                "seller_max_value": lane.seller_max_value,
                "outlier_high_rows": lane.outlier_high_rows,
                "outlier_low_rows": lane.outlier_low_rows,
                "comp_count": len(lane.rows),
            }
            for lane in lane_results
        ],
    }
    valuation_json_path.write_text(json.dumps(payload, indent=2), encoding="utf-8")
    print(f"Valuation JSON written: {valuation_json_path}")

    search_title = ""
    if inherited_filters:
        search_title = str(inherited_filters.get("search_keywords") or inherited_filters.get("search_string") or "").strip()
    if not search_title:
        search_title = " ".join(part for part in [year_filter, player_name, card_number] if part).strip() or normalized_csv_path.stem
    card_title = search_title.upper()
    primary_model = lane_results[0].selected_model_name if lane_results else ""
    subtitle = f"{sport} Valuation Report"
    if primary_model:
        subtitle = f'{subtitle}: <span class="subtitle-model">{html_lib.escape(primary_model)}</span>'

    html = _build_html_report(
        card_title=card_title,
        subtitle=subtitle,
        source_csv=normalized_csv_path,
        lane_results=lane_results,
    )
    valuation_html_path.write_text(html, encoding="utf-8")
    print(f"HTML report written: {valuation_html_path}")
    _open_html_report(valuation_html_path)


def main() -> None:
    sport = prompt_sport_scrollable()
    year_filter = prompt_year_filter_optional()

    files = list_normalized_comp_files(sport)
    files = filter_comp_files_by_year(files, year_filter)

    if not files:
        print(f"\nNo normalized CSV files found for sport={sport!r} year_filter={year_filter!r}.")
        print("Run the sold comps scraper first, or widen the year filter.")
        sys.exit(1)

    normalized_csv_path = prompt_normalized_csv_file(files)

    run_valuation_flow(
        normalized_csv_path=normalized_csv_path,
        sport=sport,
        year_filter=year_filter,
        inherited_filters=None,
    )


if __name__ == "__main__":
    main()
