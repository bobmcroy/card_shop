def _company_order(company: str) -> int:
    order = {"PSA": 0, "BGS": 1, "SGC": 2, "CGC": 3, "TAG": 4, "CSG": 5}
    return order.get(company, 99)


def _lane_section_sort_key(lane: LaneResult) -> tuple[int, int, float]:
    grader = normalize_grader(lane.grader)
    if not grader:
        return (99, 99, 99.0)

    gv = lane.grade_value
    try:
        gv_num = float(gv) if gv is not None else None
    except (TypeError, ValueError):
        gv_num = None

    if gv_num == 10.0:
        bucket_order = 0
    elif gv_num == 9.0:
        bucket_order = 1
    else:
        bucket_order = 2

    return (_company_order(grader), bucket_order, -(gv_num if gv_num is not None else -1.0))


def _display_bucket_for_lane(lane: LaneResult) -> str:
    if lane.lane_label == "eBay Raw":
        return "Raw"
    g = normalize_grader(lane.grader)
    if not g:
        return "Other"
    gv = lane.grade_value
    if gv == 10 or gv == 10.0:
        return "10"
    if gv == 9 or gv == 9.0:
        return "9"
    return "8 and below"


def _build_dashboard_groups(lane_results: list[LaneResult]) -> list[dict[str, Any]]:
    groups: list[dict[str, Any]] = []
    raw_lane = next((l for l in lane_results if l.lane_label == "eBay Raw"), None)
    if raw_lane:
        groups.append({"kind": "raw", "title": "Raw", "tiles": [raw_lane]})

    companies = sorted({normalize_grader(l.grader) for l in lane_results if normalize_grader(l.grader)}, key=_company_order)
    for company in companies:
        wanted = ["10", "9", "8 and below"]
        tiles = []
        for bucket in wanted:
            lane = next((l for l in lane_results if normalize_grader(l.grader) == company and _display_bucket_for_lane(l) == bucket), None)
            if lane:
                tiles.append({"bucket": bucket, "lane": lane})
            else:
                tiles.append({"bucket": bucket, "lane": None})
        groups.append({"kind": "company", "title": company, "tiles": tiles})
    return groups


def _arrow_html(lane: LaneResult) -> str:
    parts: list[str] = []
